        <div class="article" lang="en">
            <h3>
                I. PREPOSITIONS <a href="#ch9-e1">(*1)</a>.
            </h3>
            <p id="ch9-e1" type="note">
                (*1) <br>
                Indian grammar, which does not possess, as we do, that heterogeneous set of terms styled parts of speech, has no term exactly answering to our »prepositions,” but it calls them by different names according to their phonetical, etymological or syntactical properties. When compounded with roots, so as to make up compound verbs and the like, they are styled <i>upasarga</i>. But the same particles will be styled <i>karmapravacanîya</i>, when separate words. For this reason, the karmapravacanîya-class does not comprise such prepp., as <skt>upari</skt>, <skt>puraḥ</skt>, <skt>saha</skt>, but on the other hand it contains some particles, which cannot at any rate, be called »prepositions,” as <skt>api</skt>, <skt>su</skt>. Cp. P. 1, 4, 68; 59; 88-98.
            </p>
        </div>
        <div class="article" lang="ja">
            <h3>
                1. 前置詞<a href="#ch9-j1">（*1）</a>
            </h3>
            <p id="ch9-j1" type="note">
                （*1）<br>
                品詞（parts of speech）と呼ばれる異なるものから成る用語一式を、私たちのようには持たないインドの土着文法は、「前置詞」（preposition）に正確に対応する用語はなく、音声的、語源的、または構文的特性に応じて異なる名称で呼称される。複合動詞（compound verb）をつくるために語根と合成される場合には、<b>upasarga</b>と呼ばれる。同様の不変化辞は、〔動詞とは〕別の語である場合、<b>karmapravacanīya</b>と呼ばれる。このため、karmapravacanīya類は<skt>upari</skt>, <skt>puraḥ</skt>, <skt>saha</skt>のような前置詞を含まず、一方で<skt>api</skt>や<skt>su</skt>のような、何であれ「前置詞」とは決して呼ばれないいくつかの不変化辞を含む。P. 1, 4, 68; 59; 88-98と比較せよ。
            </p>
        </div>
